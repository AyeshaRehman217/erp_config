    public RouterFunction<ServerResponse> CalendarCategoryRoutes(CalendarCategoryHandler handle) {
        return RouterFunctions.route(GET("config/api/v1/calendar-categories/index").and(accept(APPLICATION_FORM_URLENCODED)), handle::index).filter(new IndexCalendarCategoryHandlerFilter())
                .and(RouterFunctions.route(GET("config/api/v1/calendar-categories/show/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::show).filter(new ShowCalendarCategoryHandlerFilter()))
                .and(RouterFunctions.route(POST("config/api/v1/calendar-categories/store").and(accept(APPLICATION_FORM_URLENCODED)), handle::store).filter(new StoreCalendarCategoryHandlerFilter()))
                .and(RouterFunctions.route(PUT("config/api/v1/calendar-categories/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::update).filter(new UpdateCalendarCategoryHandlerFilter()))
                .and(RouterFunctions.route(PUT("config/api/v1/calendar-categories/status/update/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::status))
                .and(RouterFunctions.route(DELETE("config/api/v1/calendar-categories/delete/{uuid}").and(accept(APPLICATION_FORM_URLENCODED)), handle::delete).filter(new DeleteCalendarCategoryHandlerFilter()));
    }
